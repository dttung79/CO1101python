# Enter 2 numbers, print out all results of math operations between 2 numbers
a = int(input('Enter a: '))
b = int(input('Enter b: '))

print('{} + {} = {}'.format(a, b, a + b))
print('{} - {} = {}'.format(a, b, a - b))
print('{} * {} = {}'.format(a, b, a * b))
print('{} / {} = {}'.format(a, b, a / b))
print('{} // {} = {}'.format(a, b, a // b))
print('{} % {} = {}'.format(a, b, a % b))
print('{} ** {} = {}'.format(a, b, a ** b))
